#include <iostream>
using namespace std;
int main() {

    int num1 = 5;

    num1 += 3; // num1 = num1 + 3
    cout << num1 << endl; // 8

    num1 -= 2; // num1 = num1 - 2
    cout << num1 << endl; // 6

    num1 *= 5; // num1 = num1 * 5
    cout << num1 << endl; // 30

    num1 /= 2; // num1 = num1 / 2
    cout << num1 << endl; // 15

    num1 %= 3; // num1 = num % 3
    cout << num1 << endl; // 0

    return 0;
}